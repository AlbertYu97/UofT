            line_number = store.enter_line(self.customer)
            # If the line is empty(First in line is the cx)
            if (store.first_in_line(line_number) and
                (store.first_in_line(line_number).name == self.customer.name)):
                checkout_event = CheckoutStarted(self.timestamp, line_number)
                return [checkout_event]
            else:
                return None
