def create_event_list(event_file: TextIO) -> list[Event]:
    """Return a list of Event objects to represent the events in <filename>.

    The events in the list must be in the same order as they are in the file.

    Preconditions:
    - <event_file> is open.
    - <event_file> is in the format specified by the assignment handout.

    >>> samp_events = create_event_list(EVENT_SAMPLE)
    >>> len(samp_events) == 3
    True
    >>> isinstance(samp_events[0], CustomerArrival)
    True
    >>> isinstance(samp_events[2], CloseLine)
    True
    """
    # TODO: Implement this function
    result = []
    for line in event_file:
        # Split the line into a list of words
        events = line.strip().split()
        time = int(events[0])
        type_name = events[1]
        if type_name == 'Arrive':
            # Add items to list
            i = 3
            item_list = []
            while i < len(events):
                item_name = events[i]
                item_time = events[i + 1]
                item = Item(item_name, item_time)
                item_list.append(item)
                i += 2
            cx = Customer(events[2], item_list)
            event = CustomerArrival(time, cx)
        else:
            # Close line
            event = CloseLine(time, int(events[2]))
        result.append(event)
    return result


